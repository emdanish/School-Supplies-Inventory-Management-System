package model;

public class Supplier {
    private int supplierId;
    private String supplierName;
    private String contactPerson;
    private String phone;
    private String email;
    private String address;
    
    // Constructors
    public Supplier() {
    }
    
    public Supplier(int supplierId, String supplierName, String contactPerson, String phone, String email, String address) {
        this.supplierId = supplierId;
        this.supplierName = supplierName;
        this.contactPerson = contactPerson;
        this.phone = phone;
        this.email = email;
        this.address = address;
    }
    
    // For creating new suppliers (ID will be auto-generated by the database)
    public Supplier(String supplierName, String contactPerson, String phone, String email, String address) {
        this.supplierName = supplierName;
        this.contactPerson = contactPerson;
        this.phone = phone;
        this.email = email;
        this.address = address;
    }
    
    // Getters and Setters
    public int getSupplierId() {
        return supplierId;
    }
    
    public void setSupplierId(int supplierId) {
        this.supplierId = supplierId;
    }
    
    public String getSupplierName() {
        return supplierName;
    }
    
    public void setSupplierName(String supplierName) {
        this.supplierName = supplierName;
    }
    
    public String getContactPerson() {
        return contactPerson;
    }
    
    public void setContactPerson(String contactPerson) {
        this.contactPerson = contactPerson;
    }
    
    public String getPhone() {
        return phone;
    }
    
    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    public String getEmail() {
        return email;
    }
    
    public void setEmail(String email) {
        this.email = email;
    }
    
    public String getAddress() {
        return address;
    }
    
    public void setAddress(String address) {
        this.address = address;
    }
    
    // For backward compatibility
    public String getContactInfo() {
        return contactPerson + "\n" + phone + "\n" + email + "\n" + address;
    }
    
    public void setContactInfo(String contactInfo) {
        // Parse contactInfo and set individual fields if needed
        if (contactInfo != null && !contactInfo.isEmpty()) {
            String[] parts = contactInfo.split("\n", 4);
            if (parts.length > 0) this.contactPerson = parts[0];
            if (parts.length > 1) this.phone = parts[1];
            if (parts.length > 2) this.email = parts[2];
            if (parts.length > 3) this.address = parts[3];
        }
    }
    
    @Override
    public String toString() {
        return supplierName;
    }
} 